<script>
export default {
  data() {
    return {
      name: "",
    };
  },
  methods: {
    add() {
      this.$emit("taskAdded", { name: this.name });
      this.name = "";
    },
  },
};
</script>     

<template>
    <div class="new-task" > 
        <input type="text" v-model="name" @keydown.enter="add" class="form-element" placeholder="Digite uma tarefa" />
        <button class="form-element" @click="add" >+</button>
    </div>
</template>
